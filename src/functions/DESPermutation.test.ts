import DESPermutation from "@/functions/DESPermutation";

test('test DESPermutation', () => {
  expect(DESPermutation('10101011001111000111110101001010')).toBe('01111100100111100111001011001100')
  expect(DESPermutation('01100100011110101010111101010011')).toBe('00010111011100111010101011011100')
  expect(DESPermutation('11011001010011100011110010110101')).toBe('00110010110010011101110011101011')
  expect(DESPermutation('11011110001000010001001011110110')).toBe('11100010101110101000010000110111')
})