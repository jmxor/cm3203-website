import DESSubstitution from "@/functions/DESSubstitution";

test('test DESSubstitution', () => {
  expect(DESSubstitution('110010000000111001101001011101000110010010101010')).toBe('11001111101110101000111111001100')
  expect(DESSubstitution('110111100011010101011100101011010000111001100010')).toBe('11101000010101001110000011101011')
  expect(DESSubstitution('010011110101000111001100110001101110011010011101')).toBe('01100111100110010110001110101001')
  expect(DESSubstitution('001010001111010110010010001101000001011111000111')).toBe('11111110011100101101101001101000')
})